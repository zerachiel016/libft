int	ft_atoi(const char *nbr)
{
	int	res;
	int	sign;

	res = 0;
	while ((9 <= *nbr && *nbr <= 13) || *nbr == 32)
		nbr++;
	sign = 1 - (2 * (*nbr == '-'));
	nbr += (*nbr == '-' || *nbr == '+');
	while ('0' <= *nbr && *nbr <= '9')
		res = res * 10 + (*nbr++ - '0');
	return (res * sign);
}
